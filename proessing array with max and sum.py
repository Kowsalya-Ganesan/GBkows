lst=list(map(int,input().split()))
sum=0
l=max(lst)
while(l):
    r=l%10
    sum=sum+r
    l=l//10
for i in range(0,len(lst)):
    k=lst[i]/sum
    print("%.2f"%k,end=" ")
















    
  

        
    
    
    
    
    
    
    
    
    
    
    

       
 
   






   
